package JavaBasics;

public class Incrementalanddecremental {

	public static void main(String[] args) {
		int i = 1 ;
		int j = ++i ;
		System.out.println(j);
		System.out.println(i);
		System.out.println("*******");
		
		int k = 1 ;
		int m = k++ ;
		System.out.println(k);
		System.out.println(m);
				
		
		
		
	

	}

}
